<h2>&nbsp;</h2>
<h1>Postseason</h1>
<p>&nbsp;</p>
<h2>Playoff Bracket</h2>
<p>&nbsp;</p>
<p>The league incorporates an eight team playoff beginning the 4th from last week of the NFL regular season (week 15 in an 18 week NFL season). The playoffs last three rounds with one round per week. The four division winners are awarded the top four seeds with the fifth and sixth seeds going to the teams with the top two remaining all-play records and the seventh and eighth seeds going to the teams with the top two remaining points-for totals over the course of the season.</p>
<p>&nbsp;</p>
<p>Any ties in regular season record are broken by regular season all-play record followed by points-for followed by regular season points-against.</p>
<p>&nbsp;</p>
<p>While the top two seeds have no bye weeks, they may choose their round one playoff opponents. If one of the top two seeds does not call out an opponent, they will play the lowest seeded team not called out.</p>
<p>&nbsp;</p>
<p>Playoffs will re-seed to ensure the highest seeds play the lowest seeds in round 2.</p>
<p>&nbsp;</p>
<h2>Toilet Bowl</h2>
<p>&nbsp;</p>
<p>The "Toilet Bowl" consists of the 8 teams with the worst all-play records that did not make the playoffs. Each team sets their opponent's lineup by announcing the lineup in the toilet-bowl channel, and matchup losers advance to the next round. If a team fails to announce their opponent's lineup, the lineup will remain as whatever the opponent had previously set.</p>
<p>&nbsp;</p>
<h2>Hamlin Rule</h2>
<p>&nbsp;</p>
<p>In the event an NFL game is postponed or canceled during championship week, the league will default to using the official NFL game book to determine the championship week scores. Alternatively, the championship match participants may, within reason, agree to an alternative solution, e.g., using a two week championship format.</p>
<h1>&nbsp;</h1>
<h1>Waivers</h1>
<p>&nbsp;</p>
<p>Volatile uses a free agent acquisition budget (FAAB) waiver system in which waiver claims are awarded in a weekly blind auction.</p>
<p>&nbsp;</p>
<p>Players are placed on waivers for two reasons:</p>
<p>&nbsp;</p>
<ul>
<li>When a player is dropped by a team: They stay on waivers for one day.</li>
</ul>
<p>&nbsp;</p>
<ul>
<li>When a player&rsquo;s game has started: They stay on waivers until Wednesday morning at 3:00 AM EST.</li>
</ul>
<p>&nbsp;</p>
<p>Once a player clears waivers, they become a free agent and can be immediately picked up. The FAAB auction processes on Wednesday morning and then opens up for teams to pick up players immediately for the remainder of the week.</p>
<p>&nbsp;</p>
<p>Waivers are closed after the championship and will not re-open until the Wednesday before week 1 of the NFL regular season. Teams will be awarded a $300 use-it-or-lose-it FAAB budget at the beginning of each season.</p>
<p>&nbsp;</p>
<h1>Trades</h1>
<p>&nbsp;</p>
<h2>Trade Block</h2>
<p>&nbsp;</p>
<p>While trades may be privately negotiated, if an offer is accepted for an asset that is on the trade block, there will be a 24 hour period during which other owners may make counter-offers. All valid counter-offers must be placed in the trade-auction channel with an @everyone tag. The asset's owner is encouraged but not required to advise the league when the owner deems a counter-offer better than the originally accepted offer.</p>
<p>&nbsp;</p>
<p>After the 24 hour counter-offer window expires, the owner has 24 hours to notify the commissioner if they would prefer to accept one of the counter-offers. If the owner does not so advise the commissioner, the original trade will process 48 hours after the initial offer was accepted.</p>
<p>&nbsp;</p>
<p>As some trades are time-sensitive, the owner of the asset on the trade block also has the right to request that the commissioner expedite the trade, forgoing the counter-offer period.</p>
<p>&nbsp;</p>
<p>Trades are allowed year-round. Trades may be vetoed by the commissioner if the commissioner reasonably believes the trade involves anti-competitive behavior. Trades may also be canceled by the commissioner if agreed upon by both parties. Trades may not be vetoed due solely to a belief that a trade involves poor judgment.</p>
<p>&nbsp;</p>
<p>Trades are not final until processed on the platform.</p>
<p>&nbsp;</p>
<h1>Dispersal Auctions</h1>
<h1>&nbsp;</h1>
<h2>Dispersal Teams</h2>
<p>&nbsp;</p>
<p>In the event that a team is without an owner at the end of the season, that team's assets will be released into a dispersal pool. Once every team in the league has an owner, recently acquired teams will participate in a dispersal draft for all available veterans.</p>
<p>&nbsp;</p>
<h2>Second Chance Teams</h2>
<p>&nbsp;</p>
<p>At the end of every season, eligible league owners may choose to release all of their assets into a dispersal pool to participate in the dispersal draft as a second chance team. Owners are eligible to participate if they have not done so in any of the previous two seasons. Second chance teams must alert the league to their intentions before the Super Bowl v&iacute;a the dispersal chat channel, and all such decisions are considered final when those intentions are announced in the dispersal chat channel.</p>
<p>&nbsp;</p>
<p>At least two teams' assets must be released into the dispersal pool for a dispersal draft to occur. Those teams may be any combination of second chance teams and recently acquired teams. DSTs will be used to represent draft picks. If all DSTs are exhausted, retired players may be used to represent additional draft picks.</p>
<p>&nbsp;</p>
<p>The commissioner will propose a time to begin the dispersal draft in the weeks following the Super Bowl. Participants should notify the commissioner if they are unable to be present at the proposed time. The commissioner will make a best effort to accommodate the scheduling needs of all participants.</p>
<p>&nbsp;</p>
<h1>Rookie Draft</h1>
<p>&nbsp;</p>
<p>A rookie draft will be held every season. The commissioner will propose a time to begin the rookie draft soon after the NFL draft. League members should notify the commissioner if they are unable to be present at the proposed time. The commissioner will make a best effort to accommodate the scheduling needs of all league members.</p>
<p>&nbsp;</p>
<p>The rookie draft is a six round, linear draft with an 8 hour clock for each selection which pauses overnight. The order of the first eight picks in each round is determined in reverse order of "Toilet Bowl" finish (Toilet Bowl King receives 1.01, runner up for last place receives 1.02, etc.). The order of pick 9-12 are determined in reverse order of regular season all-play record. The final eight picks in each round are determined in reverse order of playoff finish.</p>
<p>&nbsp;</p>
<p>Members are not required to cut down their team pre-draft to accommodate draftees, but if draftees bring the roster over the limit, the roster will be considered illegal. As such, members with illegal rosters will be unable to make free agent pickups or lineup changes until the roster is made legal again via release, trade, taxi designation, or IR designation.</p>
<h1>&nbsp;</h1>
<h1>Divisions</h1>
<p>&nbsp;</p>
<p>The league has four divisions consisting of five teams each. Beginning in the fourth season, following the rookie draft, each of the league semi-finalists from the previous season will take turns selecting which teams they would like to be in their respective divisions. The division draft will be a linear draft in reverse order of finish.This will repeat every three seasons thereafter (2027, 2030, etc).</p>
<p>&nbsp;</p>
<h1>Schedule</h1>
<p>&nbsp;</p>
<p>The commissioner may alter the schedule to ensure that each team plays every other team in their division twice and every team in one other division once. The latest week in the season with no byes will be a divisional matchup. The latest week other than that divisional matchup with no more than two teams on bye may use flexible scheduling. In that week, teams will play one team from a third division based on their places in their respective divisions on the week of the flex-scheduled matchup. Those divisional matchups will rotate annually.</p>
<p>&nbsp;</p>
<h1>League Discipline</h1>
<p>&nbsp;</p>
<h2>Uncompetitive Behavior</h2>
<p>&nbsp;</p>
<p>While Volatile is a free league with a primary goal of having fun, the league is most enjoyable for the most members when all members are actively engaged and competitive. If a member is observed to have engaged in a <em>pattern</em> of uncompetitive behavior, the commissioner may impose a 1 year probation and a bankroll fine. During probation, that league member will be under increased scrutiny to ensure the member is making their best effort to be active and competitive. If during the probationary period, the member is observed to have engaged in a continued <em>pattern</em> of uncompetitive behavior, the commissioner may remove the member and search for a replacement.</p>
<p>&nbsp;</p>
<p>Uncompetitive behavior may include but is not limited to:</p>
<ul>
<li>Missing the draft</li>
<li>Not setting lineups</li>
<li>Including injured or otherwise inactive players in a starting lineup</li>
<li>Not participating in league votes</li>
<li>Not responding to in-season trade offers</li>
</ul>
<p>&nbsp;</p>
<h2>Anti-competitive Behavior</h2>
<p>&nbsp;</p>
<p>If a league member is found to have engaged in collusion, they may be subject to removal regardless of probationary status.</p>
<p>&nbsp;</p>
<h2>Conduct Detrimental to the League</h2>
<h2>&nbsp;</h2>
<h2>League members may be removed at any time if they engage in conduct detrimental to the league. Such conduct includes but is not limited to:</h2>
<h2>&nbsp;</h2>
<h2>-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Harassment or abusive behavior</h2>
<h2>-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; A pattern of unresponsiveness</h2>
<h2>&nbsp;</h2>
<h2>Removal</h2>
<p>&nbsp;</p>
<p>In the event of a decision to remove a member, after informing the offending member of the decision, the member will have a minimum of 48 hours to appeal the decision.</p>
<p>&nbsp;</p>
<h1>Amendment</h1>
<p>&nbsp;</p>
<p>This league charter may generally be amended if approved by at least 11 members. The commissioner will only vote on charter amendments in the event of a tie. The commissioner has sole discretion on which amendments may be brought to a vote but acknowledges that a league should generally take a form most desirable to its members. If a proposal fails, an identical or nearly identical proposal may generally not be brought to a vote the following season.</p>
<p>&nbsp;</p>
<p>If a poll has more than two competing options, voting may be held in rounds with one option eliminated each round until one of the remaining options gains 11 or more votes.</p>
<p>&nbsp;</p>
<p>Any changes to postseason format or rules must generally be agreed upon prior to the regular season. Any changes to draft format or rules must generally be agreed upon prior to the previous regular season. Any changes to regular season format or rules must generally be agreed upon prior to the previous regular season.</p>
<p>&nbsp;</p>
<p>Additionally, if the commissioner deems it to be in the best interest of the league, the commissioner may make changes to the league charter that are considered either too minor or too important or urgent to bring to a vote. The commissioner must always inform the league of these changes in a timely manner.&nbsp;</p>
<p>&nbsp;</p>
<h1>Trigger Amendments</h1>
<p>&nbsp;</p>
<p>The following amendment will automatically be approved pending platform support. This trigger amendment may be nullified before implementation by a vote of 15 league members.</p>
<p>&nbsp;</p>
<p>In the event that Sleeper adds more IDP positions, e.g., DE, DT, EDGE, S, CB, the league will adapt our current positions to those more specific positions.</p>
